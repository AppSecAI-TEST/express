package cn.raoxianhua.dms.vo;

/**
 * @author raoxianhua
 *
 */
public class Staff {
	
	private String sid;
	private String spwd;
	private String sname;
	private String snumber;
	private String slphone;
	private String sremark;
	/**
	 * @return the sid
	 */
	public String getSid() {
		return sid;
	}
	/**
	 * @param sid the sid to set
	 */
	public void setSid(String sid) {
		this.sid = sid;
	}
	/**
	 * @return the spwd
	 */
	public String getSpwd() {
		return spwd;
	}
	/**
	 * @param spwd the spwd to set
	 */
	public void setSpwd(String spwd) {
		this.spwd = spwd;
	}
	/**
	 * @return the sname
	 */
	public String getSname() {
		return sname;
	}
	/**
	 * @param sname the sname to set
	 */
	public void setSname(String sname) {
		this.sname = sname;
	}
	/**
	 * @return the snumber
	 */
	public String getSnumber() {
		return snumber;
	}
	/**
	 * @param snumber the snumber to set
	 */
	public void setSnumber(String snumber) {
		this.snumber = snumber;
	}
	/**
	 * @return the slphone
	 */
	public String getSlphone() {
		return slphone;
	}
	/**
	 * @param slphone the slphone to set
	 */
	public void setSlphone(String slphone) {
		this.slphone = slphone;
	}
	/**
	 * @return the sremark
	 */
	public String getSremark() {
		return sremark;
	}
	/**
	 * @param sremark the sremark to set
	 */
	public void setSremark(String sremark) {
		this.sremark = sremark;
	}
	
}
