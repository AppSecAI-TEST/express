package cn.raoxianhua.dms.vo;

/**
 * @author raoxianhua
 *
 */
public class Client {
	
	private String cid;
	private String cpwd;
	private String cname;
	private String clphone;
	private String cemail;
	/**
	 * @return the cid
	 */
	public String getCid() {
		return cid;
	}
	/**
	 * @param cid the cid to set
	 */
	public void setCid(String cid) {
		this.cid = cid;
	}
	/**
	 * @return the cpwd
	 */
	public String getCpwd() {
		return cpwd;
	}
	/**
	 * @param cpwd the cpwd to set
	 */
	public void setCpwd(String cpwd) {
		this.cpwd = cpwd;
	}
	/**
	 * @return the cname
	 */
	public String getCname() {
		return cname;
	}
	/**
	 * @param cname the cname to set
	 */
	public void setCname(String cname) {
		this.cname = cname;
	}
	/**
	 * @return the clphone
	 */
	public String getClphone() {
		return clphone;
	}
	/**
	 * @param clphone the clphone to set
	 */
	public void setClphone(String clphone) {
		this.clphone = clphone;
	}
	/**
	 * @return the cemail
	 */
	public String getCemail() {
		return cemail;
	}
	/**
	 * @param cemail the cemail to set
	 */
	public void setCemail(String cemail) {
		this.cemail = cemail;
	}

}
